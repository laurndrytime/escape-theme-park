import {defineArrayMember, defineField, defineType} from '@sanity-typed/types'

export default defineType({
  name: 'post',
  title: 'Post',
  type: 'document',
  fields: [
    defineField({
      name: 'title',
      title: 'Title',
      type: 'string',
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'title',
        maxLength: 96,
      },
    }),
    defineField({
      name: 'profile',
      title: 'Profile',
      type: 'reference',
      //@ts-ignore
      to: {type: 'profile' as const},
    }),
    defineField({
      name: 'mainImage',
      title: 'Main image',
      type: 'image',
      options: {
        hotspot: true,
      },
    }),
    defineField({
      name: 'categories',
      title: 'Categories',
      type: 'array',
      of: [
        defineArrayMember({
          type: 'reference',
          //@ts-ignore
          to: {type: 'category'},
        }),
      ],
    }),
    defineField({
      name: 'publishedAt',
      title: 'Published at',
      type: 'datetime',
    }),
    defineField({
      name: 'body',
      title: 'Body',
      type: 'blockContent',
    }),
  ],

  preview: {
    select: {
      title: 'title',
      profile: 'profile.name',
      media: 'mainImage',
    },
    prepare(selection) {
      const {profile} = selection
      return {...selection, subtitle: profile && `by ${profile}`}
    },
  },
})
